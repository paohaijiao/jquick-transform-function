import com.github.paohaijiao.function.domain.JQuickBaseFunctionFunctionProvider;
import com.github.paohaijiao.spi.anno.Priority;
import com.github.paohaijiao.spi.constants.PriorityConstants;
import java.util.List;
import java.util.Random;

@Priority(PriorityConstants.SYSTEM_HIGH)
public class JQuickRandomStringFunctionProvider extends JQuickBaseFunctionFunctionProvider {

    private static final String CHARS = "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789";
    private static final Random RANDOM = new Random();

    public JQuickRandomStringFunctionProvider() {
        super("randomString", "生成随机字符串 - 用法: randomString(length)");
    }

    @Override
    public Object invoke(List<Object> args) {
        validateArgCount(args, 1);
        int length = asInt(args.get(0));
        if (length <= 0) return "";
        StringBuilder sb = new StringBuilder(length);
        for (int i = 0; i < length; i++) {
            sb.append(CHARS.charAt(RANDOM.nextInt(CHARS.length())));
        }
        return sb.toString();
    }
}
